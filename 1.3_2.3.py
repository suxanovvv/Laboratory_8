#Прямий пошук підрядка.
while True:
    text=input('Введіть текст: ')
    print(text)
    pattern=input('Введіть підрядок: ')
    print(pattern)

    i=-1 #індекс рядка.
    j=0 #індекс підрядка.
    counter=-1 #лічильник

    while (j<len(pattern)) and i<(len(text)-len(pattern)):
        j=0
        i+=1
        counter+=1
        while j<len(pattern) and pattern[j]==text[i+j]:
            j+=1
    if (j==len(pattern)):
        print(f'Підрядок знайдений у позиції {i} за {counter} зміщень.')
    else:
        print('Підрядок не знайдено.')


    result=input('Input "1" to continue, and other to exit: ')
    if result=='1':
        continue
    break

